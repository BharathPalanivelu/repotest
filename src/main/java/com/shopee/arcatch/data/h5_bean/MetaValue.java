package com.shopee.arcatch.data.h5_bean;

public class MetaValue {
    private String value;

    public String getValue() {
        return this.value;
    }

    public void setValue(String str) {
        this.value = str;
    }

    public String toString() {
        return "MetaValue{value='" + this.value + '\'' + '}';
    }
}
